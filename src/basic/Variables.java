package basic;

public class Variables {

	public static void main(String[] args) {
		
		// All primitive data types
		byte value_byte = 34;
		short value_short = 45;
		int value_integer = 9484;
		float value_float = 63.76f;
		double value_double = 73.8;
		long value_long = 83;
		
		char value_character = 'a';

		boolean value_boolean = true;
		
		// non primitive data types
		String value_string = "Subhranil";
		
		System.out.println(value_byte);
		System.out.println(value_short);
		System.out.println(value_integer);
		System.out.println(value_float);
		System.out.println(value_double);
		System.out.println(value_long);
		System.out.println(value_character);
		System.out.println(value_boolean);
		System.out.println(value_string);
		
	}

}
